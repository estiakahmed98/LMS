// Mock data for PSTC LMS - No backend/database required
// This mirrors the Prisma schema structure for reference

export type Role =
  | "SUPER_ADMIN"
  | "COURSE_MANAGER"
  | "EXAMINER"
  | "REPORT_VIEWER"
  | "STUDENT";
export type Status =
  | "PENDING"
  | "APPROVED"
  | "REJECTED"
  | "SUSPENDED"
  | "ACTIVE"
  | "INACTIVE";
export type EnrollmentStatus =
  | "PENDING"
  | "APPROVED"
  | "REJECTED"
  | "WITHDRAWN";
export type AssessmentType = "MCQ" | "WRITTEN" | "PRACTICAL" | "MIXED";
export type SubmissionStatus =
  | "DRAFT"
  | "SUBMITTED"
  | "GRADING"
  | "GRADED"
  | "REVIEWED";

export interface User {
  id: string;
  name: string;
  email: string;
  phone?: string;
  role: Role;
  status: Status;
  createdAt: Date;
  lastActive?: Date;
}

export interface Course {
  id: string;
  title: string;
  description: string;
  duration: number; // in hours
  level: "BEGINNER" | "INTERMEDIATE" | "ADVANCED";
  createdAt: Date;
}

export interface Module {
  id: string;
  courseId: string;
  title: string;
  order: number;
  type: "VIDEO" | "READING" | "QUIZ" | "PRACTICE";
  duration: number; // minutes
}

export interface Enrollment {
  id: string;
  userId: string;
  courseId: string;
  status: EnrollmentStatus;
  progress: number; // 0-100
  enrolledAt: Date;
  completedAt?: Date;
}

export interface Assessment {
  id: string;
  courseId: string;
  title: string;
  type: AssessmentType;
  totalMarks: number;
  passingMarks: number;
  createdAt: Date;
}

export interface Question {
  id: string;
  assessmentId: string;
  type: "MCQ" | "WRITTEN" | "PRACTICAL";
  question: string;
  marks: number;
  options?: string[];
  correctAnswer?: string;
  rubric?: string;
}

export interface Submission {
  id: string;
  assessmentId: string;
  userId: string;
  status: SubmissionStatus;
  obtainedMarks?: number;
  submittedAt?: Date;
  gradedAt?: Date;
  answerSheetUrls?: string[];
}

export interface Certificate {
  id: string;
  userId: string;
  courseId: string;
  issueDate: Date;
  certificateNumber: string;
}

export interface Notification {
  id: string;
  userId: string;
  title: string;
  message: string;
  type: "INFO" | "WARNING" | "SUCCESS" | "ERROR";
  readAt?: Date;
  createdAt: Date;
}

export interface AuditLog {
  id: string;
  userId: string;
  action: string;
  entity: string;
  entityId: string;
  changes?: Record<string, unknown>;
  createdAt: Date;
}

